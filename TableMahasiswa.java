        private void tabel (){
        DefaultTableModel t= new DefaultTableModel();
        t.addColumn("Nim");
        t.addColumn("Nama");
        t.addColumn("Jenis Kelamin");
        t.addColumn("No HP");
        t.addColumn("Alamat");
        kelompok_table.setModel(t); try{ res= stat.executeQuery("select * from mahasiswa");
        while (res.next()) {
        t.addRow(new Object[]{ res.getString("Nim"),
      res.getString("Nama"),
      res.getString("Jenis kelamin"),
      res.getString("NoHp"),
      res.getString("Alamat")
        });
    }
    }   catch (Exception e) {
    JOptionPane.showMessageDialog(rootPane, e);
    }}

