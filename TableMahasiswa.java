    private void kelompok_mahasiswaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kelompok_mahasiswaActionPerformed
        // TODO add your handling code here:
        int index = kelompok_table.getSelectedRow();
        
        TableModel model = kelompok_table.getModel();
        
        String nim = model.getValueAt(index, 0).toString();
        String nama = model.getValueAt(index, 1).toString();
        String jk = model.getValueAt(index, 2).toString();
        String nohp = model.getValueAt(index, 3).toString();
        String alamat = model.getValueAt(index, 4).toString();
      
       
        data.setVisible(true);
        data.pack();
        data.setLocationRelativeTo(null);
        data.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        
        data.kelompok_nim.setText(nim);
        data.kelompok_nama.setText(nama);
        data.kelompok_jk.setSelectedItem(jk);
        data.kelompok_nohp.setText(nohp);
        data.kelompok_alamat.setText(alamat);
        dispose();
    }//GEN-LAST:event_kelompok_mahasiswaActionPerformed
